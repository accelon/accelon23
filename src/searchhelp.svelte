原序/重要/重覆/句頻/句長：設定句子排序方式，重要性為句頻句長乘積，同時出現的句子次數越多重覆性越高<br/>
=/≃/≈數字：此句出現時，100%, 90%, 80%會同時出現之句數。<br/>
全句：點句子括號數字，顯示含完整該句的行。
